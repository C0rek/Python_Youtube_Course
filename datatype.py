
# Strings
print("Hello world")    # imprime lo mismo
print('Hello world')
print('''Hello world''')
print("""Hello world""")

# imprimir el tipo de archivo
print(type("Hello world")) # devuelve el 'str' que significa string 

# Unir String/Concatenar
print("Bye" + "World")

# Numbers
print(30) # Int
print(30.5) #String

#Boolean (Tipo de estado)
print(type(True)) # Bool
print(type(False)) # Bool  

# List (Lista de datos)
# num aleatorios
[10, 20, 30, 55]  # se puede cambiar
["Hello", "Bye", "Adios"]
[10, "Hello", True,10, 1] # Tambien pueden ser mixtos
[]  # Listavacia

# Tumples
(10, 20, 30, 55) # NO se puede cambiar (INMUTABLE)
()

